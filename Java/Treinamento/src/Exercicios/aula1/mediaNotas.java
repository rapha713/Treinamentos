package Exercicios.aula1;

public class mediaNotas {
    public static void main(String[] args) {
        double nota1 = 10.0;
        double nota2 = 6.5;
        double nota3 = 7.0;

        double media = (nota1 + nota2 + nota3) / 3;

        System.out.println("A média das notas é: " + media);
    }
}
