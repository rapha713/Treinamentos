package Exercicios.aula1;

public class converterIdade {
    public static void main(String[] args){
        int idade = 29;

        int meses = idade * 12;
        int dias = idade * 365;

        System.out.println("Idade: " + idade);
        System.out.println("Idade em meses: " + meses);
        System.out.println("Idade em dias: " + dias);
    }
}
